public class Example02_02 {

    public static void main(String[] args) {
        System.out.println(123);
        System.out.println(123l);
        System.out.println(0123);
        System.out.println(0x123);
        System.out.println(123.50);
        System.out.println(123.5f);
        System.out.println(123e3); //ผลออกมาเป็นทศนิยม
        System.out.println(123e-2); //ผลออกมาเป็นทศนิยม
    }
}